/**
 * Creates a styled paragraph
 */
export function createStyledParagraph(text, color = 'default', bold = false, italic = false) {
  return {
    object: 'block',
    type: 'paragraph',
    paragraph: {
      rich_text: [{
        type: 'text',
        text: { content: text },
        annotations: { bold, italic, color },
      }],
    },
  };
}

/**
 * Creates a callout with icon and content
 */
export function createCallout(content, emoji = 'ğŸ’¡', color = 'gray_background') {
  return {
    object: 'block',
    type: 'callout',
    callout: {
      rich_text: [{
        type: 'text',
        text: { content },
      }],
      icon: { type: 'emoji', emoji },
      color,
    },
  };
}

/**
 * Creates a toggle block with content
 */
export function createToggle(title, children = []) {
  return {
    object: 'block',
    type: 'toggle',
    toggle: {
      rich_text: [{
        type: 'text',
        text: { content: title },
        annotations: { bold: true },
      }],
      children,
    },
  };
}

