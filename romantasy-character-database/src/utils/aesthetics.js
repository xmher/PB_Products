/**
 * Creates a decorative header block
 */
export function createDecorativeHeader(text, dividerType = 'stars') {
  return [
    {
      object: 'block',
      type: 'paragraph',
      paragraph: {
        rich_text: [{
          type: 'text',
          text: { content: DIVIDERS[dividerType] },
          annotations: { color: 'gray' },
        }],
      },
    },
    {
      object: 'block',
      type: 'heading_2',
      heading_2: {
        rich_text: [{
          type: 'text',
          text: { content: text },
        }],
      },
    },
  ];
}

/**
 * Creates a quote callout block
 */
export function createQuoteCallout(quoteKey, icon = 'ğŸ“–') {
  const quote = QUOTES[quoteKey] || QUOTES.writing;
  return {
    object: 'block',
    type: 'callout',
    callout: {
      rich_text: [{
        type: 'text',
        text: { content: quote },
        annotations: { italic: true, color: 'gray' },
      }],
      icon: { type: 'emoji', emoji: icon },
      color: 'gray_background',
    },
  };
}

